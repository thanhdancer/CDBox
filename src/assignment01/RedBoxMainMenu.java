    private void btnSearchVideoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchVideoActionPerformed
        // TODO add your handling code here:
        int type = cboTypeVideo.getSelectedIndex();
        String query = txtSearchVideo.getText();
        String result = "";
        MovieCD tmp = null;
        switch (type){
            case 0: //search by code                
                tmp = bMovie.searchByCode(query);
                break;
            case 1: // seach by title
                tmp = bMovie.searchByTitle(query);
                break;
            case 2: // seach by price
                try {
                    tmp = bMovie.searchByPrice(Double.parseDouble(query));
                } catch (Exception e) {
                    System.out.println(e.toString());
                }
                break;
        }
        if(tmp != null){
            result += "Code: " + tmp.getCode() + "\n";
            result += "Title: " + tmp.getTitle()+ "\n";
            result += "Price: " + tmp.getPrice()+ "\n";
        }else{
            result += "Not found";
        }
        txtResultVideo.setText(result);
    }//GEN-LAST:event_btnSearchVideoActionPerformed

