    private void btnSearchMusicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchMusicActionPerformed
        // TODO add your handling code here:
        int type = cboTypeMusic.getSelectedIndex();
        String query = txtSearchMusic.getText();
        String result = "";
        MusicCD tmp = null;
        switch (type){
            case 0: //search by code                
                tmp = bMusic.searchByCode(query);
                break;
            case 1: // seach by title
                tmp = bMusic.searchByTitle(query);
                break;
            case 2: // seach by price
                try {
                    tmp = bMusic.searchByPrice(Double.parseDouble(query));
                } catch (Exception e) {
                    System.out.println(e.toString());
                }
                break;
        }
        if(tmp != null){
            result += "Code: " + tmp.getCode() + "\n";
            result += "Title: " + tmp.getTitle()+ "\n";
            result += "Artist: " + tmp.getArtist()+ "\n";
            result += "Price: " + tmp.getPrice()+ "\n";
        }else{
            result += "Not found";
        }
        txtResultMusic.setText(result);
    }//GEN-LAST:event_btnSearchMusicActionPerformed

    
