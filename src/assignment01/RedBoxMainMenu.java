    private void btnImportVideoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportVideoActionPerformed
        // TODO add your handling code here:
        File fileHandle = null;
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileNameExtensionFilter("Data file", "dat"));
        int choice = chooser.showOpenDialog(jLabel1);
        if(choice == JFileChooser.APPROVE_OPTION){
            fileHandle = chooser.getSelectedFile();            
        }
        
        if(fileHandle != null){
            String filePath = fileHandle.getPath();
            bMovie.loadAll(filePath);
            clearVideoTable();

            int size = bMovie.getSize();
            for(int i = 0; i< size; i++){
                dtmVideo.addRow(bMovie.getMovie(i).toArray());
            }
        }
        
    }//GEN-LAST:event_btnImportVideoActionPerformed

    private void clearVideoTable(){
        int rows = dtmVideo.getRowCount();
        for(int i = 0; i< rows; i++){
            dtmVideo.removeRow(0);
        }
    }
    
    private void clearMusicTable(){
        int rows = dtmMusic.getRowCount();
        for(int i = 0; i < rows; i++ ){
            dtmMusic.removeRow(0);
        }
    }
    
